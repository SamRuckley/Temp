#ifndef GAMES_H
#define GAMES_H

/////////////////////////////////////////////////////////////////////////
//	Includes
/////////////////////////////////////////////////////////////////////////
#include <iostream>
#include <string>

#include "UserInput.h"

//
//	@summary	Handles all the games.
//
class Games
{
	
	/////////////////////////////////////////////////////////////////////////
	//	Public
	/////////////////////////////////////////////////////////////////////////
	public:
		/////////////////////////////////////////////////////////////////////////
		//	Types
		/////////////////////////////////////////////////////////////////////////

		//
		//	@summary	Player
		//
		enum PlayerGameType
		{
			SinglePlayer,
			TwoPlayer,
			Unknown
		};

		/////////////////////////////////////////////////////////////////////////
		//	Variables
		/////////////////////////////////////////////////////////////////////////

		/////////////////////////////////////////////////////////////////////////
		//	Methods
		/////////////////////////////////////////////////////////////////////////

		//
		//	@summary	Constructor.
		//
		Games();

		//
		//	@summary	Destructor.
		//
		virtual ~Games();
		
		//
		//	@summary	Gets the name of the game.
		//	@return		The game name.
		//
		virtual std::string GetName() = 0;
		
		//
		//	@summary	Gets the player game type of the game.
		//	@return		The player game type.
		//
		virtual PlayerGameType GetPlayerGameType() = 0;

		//
		//	@summary	Runs the game menu.
		//
		virtual void GameMenu() = 0;
};
#endif // GAMES_H 
